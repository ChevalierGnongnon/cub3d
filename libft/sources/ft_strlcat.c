#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dlen;

	dlen = ft_strlen(dst);
	i = 0;
	if (dlen > size || size == 0)
		return (ft_strlen(src) + size);
	while ((src[i] != '\0') && dlen + i < size - 1)
	{
		dst[dlen + i] = src[i];
		i++;
	}
	dst[dlen + i] = '\0';
	return (dlen + ft_strlen(src));
}
